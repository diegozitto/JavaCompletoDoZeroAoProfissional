package Modulo1_Fundamentos.fundamentos.primitivos;

public class TiposPrimitivos {
    public static void main(String[] args) {
        /*
        8 tipos primitivos (6 numéricos e 2 textos):

        1.byte (1 byte - 8 bits)
        2.short (2 byte)
        3.int (4 byte)
        4.long (8 byte)

        5.float (4 byte)
        6.double (8 byte)

        7.char ('1 caractere')
        8.boolean (true / false)

        Conversão de um tipo menor para um maior: OK
         */

        // tipos numéricos inteiros
        byte b = 23;
        short s = 542;
        int i = 56789;
        long l = 3_134_845_223L;

        // tipos numéricos reais
        float f = 11_445.44F;
        double d = 2_991_797_103.01;

        // tipo booleano
        boolean tF = false;

        // tipo caractere
        char ch = 'a';
    }
}
