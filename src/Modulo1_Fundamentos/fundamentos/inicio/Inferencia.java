package Modulo1_Fundamentos.fundamentos.inicio;

public class Inferencia {
    public static void main(String[] args) {

        // float
        var a = 4.5f;

        // inteiro
        var b = 5;

        // boolean
        var c = true;

        // double
        var d = 45.6d;

        // String ou texto
        var e = "String";

        // char
        var f = 'r';

        /*
        A inferência de tipo é uma característica de algumas linguagens de
        programação que permite que o compilador deduza o tipo de uma variável
        ou construção, sem que o programador precise especificar explicitamente

        A inferência não permite mudar o tipo de uma inferência após ter sido definido

        A variável inferida precisa ser definida na mesma linha em  que foi criada.
         */

    }
}
