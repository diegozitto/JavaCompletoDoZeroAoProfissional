package Modulo2_EstruturasDeControle.controle.aulas;

public class Aula7_For2 {
    public static void main(String[] args) {
        // Desafio: criar um laço for decrecescente (de 10 a 0)
        // porém decrementando de 2 em 2

        Integer i;
        for (i = 10; i >= 0; i -=2){
            System.out.println("i: " + i);
        }
    }
}
